package com.qts.icam.model.ticket;

public class TaskComment {

	private String taskCommentObjectId;
	private String taskCommentCode;
	private String taskCommentDesc;
	private String updatedBy;
	
	private int taskCommentId;
	private String comment;
	private String taskCommentDate;
	
	
	public String getTaskCommentObjectId() {
		return taskCommentObjectId;
	}
	public void setTaskCommentObjectId(String taskCommentObjectId) {
		this.taskCommentObjectId = taskCommentObjectId;
	}
	public String getTaskCommentCode() {
		return taskCommentCode;
	}
	public void setTaskCommentCode(String taskCommentCode) {
		this.taskCommentCode = taskCommentCode;
	}
	public String getTaskCommentDesc() {
		return taskCommentDesc;
	}
	public void setTaskCommentDesc(String taskCommentDesc) {
		this.taskCommentDesc = taskCommentDesc;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	public int getTaskCommentId() {
		return taskCommentId;
	}
	public void setTaskCommentId(int taskCommentId) {
		this.taskCommentId = taskCommentId;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getTaskCommentDate() {
		return taskCommentDate;
	}
	public void setTaskCommentDate(String taskCommentDate) {
		this.taskCommentDate = taskCommentDate;
	}
}
