package com.qts.icam.model.ticket;

public class TicketComment {
	private String ticketCommentObjectId;
	private String ticketCommentCode;
	private String ticketCommentDesc;
	private String updatedBy;
	
	private int ticketCommentId;
	private String comment;
	private String ticketCommentDate;
	
	
	public String getTicketCommentObjectId() {
		return ticketCommentObjectId;
	}
	public void setTicketCommentObjectId(String ticketCommentObjectId) {
		this.ticketCommentObjectId = ticketCommentObjectId;
	}
	public String getTicketCommentCode() {
		return ticketCommentCode;
	}
	public void setTicketCommentCode(String ticketCommentCode) {
		this.ticketCommentCode = ticketCommentCode;
	}
	public String getTicketCommentDesc() {
		return ticketCommentDesc;
	}
	public void setTicketCommentDesc(String ticketCommentDesc) {
		this.ticketCommentDesc = ticketCommentDesc;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	public int getTicketCommentId() {
		return ticketCommentId;
	}
	public void setTicketCommentId(int ticketCommentId) {
		this.ticketCommentId = ticketCommentId;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getTicketCommentDate() {
		return ticketCommentDate;
	}
	public void setTicketCommentDate(String ticketCommentDate) {
		this.ticketCommentDate = ticketCommentDate;
	}
	
	
}
