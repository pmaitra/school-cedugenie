package com.qts.icam.model.library;

/**
 * @author anup.roy*/

public class MagazinePublisher {

	private String magazinePublisherObjId;
	private String updatedBy;
	private String magazinePublisherCode;
	private String magazinePublisherName;
	/**
	 * @return the magazinePublisherObjId
	 */
	public String getMagazinePublisherObjId() {
		return magazinePublisherObjId;
	}
	/**
	 * @param magazinePublisherObjId the magazinePublisherObjId to set
	 */
	public void setMagazinePublisherObjId(String magazinePublisherObjId) {
		this.magazinePublisherObjId = magazinePublisherObjId;
	}
	/**
	 * @return the updatedBy
	 */
	public String getUpdatedBy() {
		return updatedBy;
	}
	/**
	 * @param updatedBy the updatedBy to set
	 */
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	/**
	 * @return the magazinePublisherCode
	 */
	public String getMagazinePublisherCode() {
		return magazinePublisherCode;
	}
	/**
	 * @param magazinePublisherCode the magazinePublisherCode to set
	 */
	public void setMagazinePublisherCode(String magazinePublisherCode) {
		this.magazinePublisherCode = magazinePublisherCode;
	}
	/**
	 * @return the magazinePublisherName
	 */
	public String getMagazinePublisherName() {
		return magazinePublisherName;
	}
	/**
	 * @param magazinePublisherName the magazinePublisherName to set
	 */
	public void setMagazinePublisherName(String magazinePublisherName) {
		this.magazinePublisherName = magazinePublisherName;
	}
	
	
}
