package com.qts.icam.model.library;



public class BookPublisher {
	private String bookPublisherObjectId;
	private String updatedBy;
	private String bookPublisherName;
	private String bookPublisherCode;
	private String bookPublisherDesc;
	
	public String getBookPublisherObjectId() {
		return bookPublisherObjectId;
	}
	public void setBookPublisherObjectId(String bookPublisherObjectId) {
		this.bookPublisherObjectId = bookPublisherObjectId;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	public String getBookPublisherName() {
		return bookPublisherName;
	}
	public void setBookPublisherName(String bookPublisherName) {
		this.bookPublisherName = bookPublisherName;
	}
	public String getBookPublisherCode() {
		return bookPublisherCode;
	}
	public void setBookPublisherCode(String bookPublisherCode) {
		this.bookPublisherCode = bookPublisherCode;
	}
	public String getBookPublisherDesc() {
		return bookPublisherDesc;
	}
	public void setBookPublisherDesc(String bookPublisherDesc) {
		this.bookPublisherDesc = bookPublisherDesc;
	}
	

}
