public class BookMedium {

	private String bookMediumObjectId;
	private String bookMediumCode;
	private String bookMediumName;
	private String bookMediumDesc;
	private String updatedBy;

	public String getBookMediumObjectId() {
		return bookMediumObjectId;
	}

	public void setBookMediumObjectId(String bookMediumObjectId) {
		this.bookMediumObjectId = bookMediumObjectId;
	}

	public String getBookMediumCode() {
		return bookMediumCode;
	}

	public void setBookMediumCode(String bookMediumCode) {
		this.bookMediumCode = bookMediumCode;
	}

	public String getBookMediumName() {
		return bookMediumName;
	}

	public void setBookMediumName(String bookMediumName) {
		this.bookMediumName = bookMediumName;
	}

	public String getBookMediumDesc() {
		return bookMediumDesc;
	}

	public void setBookMediumDesc(String bookMediumDesc) {
		this.bookMediumDesc = bookMediumDesc;
	}

	public String getUpdatedBy() {
		return updatedBy;
	}

	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}

}
