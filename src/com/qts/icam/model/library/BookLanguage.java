package com.qts.icam.model.library;

/**
 * @author saikat.das
 * 
 */
public class BookLanguage {

	private String bookLanguageObjectId;
	private String bookLanguageCode;
	private String bookLanguageName;
	private String bookLanguageDesc;
	private String updatedBy;

	public String getBookLanguageObjectId() {
		return bookLanguageObjectId;
	}

	public void setBookLanguageObjectId(String bookLanguageObjectId) {
		this.bookLanguageObjectId = bookLanguageObjectId;
	}

	public String getBookLanguageCode() {
		return bookLanguageCode;
	}

	public void setBookLanguageCode(String bookLanguageCode) {
		this.bookLanguageCode = bookLanguageCode;
	}

	public String getBookLanguageName() {
		return bookLanguageName;
	}

	public void setBookLanguageName(String bookLanguageName) {
		this.bookLanguageName = bookLanguageName;
	}

	public String getBookLanguageDesc() {
		return bookLanguageDesc;
	}

	public void setBookLanguageDesc(String bookLanguageDesc) {
		this.bookLanguageDesc = bookLanguageDesc;
	}

	public String getUpdatedBy() {
		return updatedBy;
	}

	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}

}
