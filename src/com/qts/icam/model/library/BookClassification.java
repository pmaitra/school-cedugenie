public class BookClassification {

	private String bookClassificationObjectId;
	private String bookClassificationCode;
	private String bookClassificationName;
	private String bookClassificationDesc;
	private String bookClassificationClass;
	private String bookClassificationDept;
	private String bookClassificationDivision;
	private String updatedBy;

	public String getBookClassificationObjectId() {
		return bookClassificationObjectId;
	}

	public void setBookClassificationObjectId(String bookClassificationObjectId) {
		this.bookClassificationObjectId = bookClassificationObjectId;
	}

	public String getBookClassificationCode() {
		return bookClassificationCode;
	}

	public void setBookClassificationCode(String bookClassificationCode) {
		this.bookClassificationCode = bookClassificationCode;
	}

	public String getBookClassificationName() {
		return bookClassificationName;
	}

	public void setBookClassificationName(String bookClassificationName) {
		this.bookClassificationName = bookClassificationName;
	}

	public String getBookClassificationDesc() {
		return bookClassificationDesc;
	}

	public void setBookClassificationDesc(String bookClassificationDesc) {
		this.bookClassificationDesc = bookClassificationDesc;
	}

	public String getBookClassificationClass() {
		return bookClassificationClass;
	}

	public void setBookClassificationClass(String bookClassificationClass) {
		this.bookClassificationClass = bookClassificationClass;
	}

	public String getBookClassificationDept() {
		return bookClassificationDept;
	}

	public void setBookClassificationDept(String bookClassificationDept) {
		this.bookClassificationDept = bookClassificationDept;
	}

	public String getBookClassificationDivision() {
		return bookClassificationDivision;
	}

	public void setBookClassificationDivision(String bookClassificationDivision) {
		this.bookClassificationDivision = bookClassificationDivision;
	}

	public String getUpdatedBy() {
		return updatedBy;
	}

	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}

}
