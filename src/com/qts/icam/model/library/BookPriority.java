package com.qts.icam.model.library;

/**
 * @author saikat.das
 * 
 */
public class BookPriority {

	private String bookPriorityObjectId;
	private String bookPriorityCode;
	private String bookPriorityDesc;
	private String updatedBy;

	public String getBookPriorityObjectId() {
		return bookPriorityObjectId;
	}

	public void setBookPriorityObjectId(String bookPriorityObjectId) {
		this.bookPriorityObjectId = bookPriorityObjectId;
	}

	public String getBookPriorityCode() {
		return bookPriorityCode;
	}

	public void setBookPriorityCode(String bookPriorityCode) {
		this.bookPriorityCode = bookPriorityCode;
	}

	public String getBookPriorityDesc() {
		return bookPriorityDesc;
	}

	public void setBookPriorityDesc(String bookPriorityDesc) {
		this.bookPriorityDesc = bookPriorityDesc;
	}

	public String getUpdatedBy() {
		return updatedBy;
	}

	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}

}
