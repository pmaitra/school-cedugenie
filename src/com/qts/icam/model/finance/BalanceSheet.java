package com.qts.icam.model.finance;

public class BalanceSheet {
	
	String ledger;
	Double amount;
	String assetLiablity;
	
	
	public String getLedger() {
		return ledger;
	}
	public void setLedger(String ledger) {
		this.ledger = ledger;
	}
	public Double getAmount() {
		return amount;
	}
	public void setAmount(Double amount) {
		this.amount = amount;
	}
	public String getAssetLiablity() {
		return assetLiablity;
	}
	public void setAssetLiablity(String assetLiablity) {
		this.assetLiablity = assetLiablity;
	}
	
	

}
