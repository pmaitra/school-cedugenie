package com.qts.icam.model.admission;

public class Marks {

	private String strSubjectName;
	private int intSubjectMarks;
	private String strMarksObjId;
	private String strFormId;
	private String admissionYear;
	private String formName;
	private String courseClass;
	
	private String updatedBy;
	
	public String getStrSubjectName() {
		return strSubjectName;
	}
	public void setStrSubjectName(String strSubjectName) {
		this.strSubjectName = strSubjectName;
	}
	public int getIntSubjectMarks() {
		return intSubjectMarks;
	}
	public void setIntSubjectMarks(int intSubjectMarks) {
		this.intSubjectMarks = intSubjectMarks;
	}
	public String getStrMarksObjId() {
		return strMarksObjId;
	}
	public void setStrMarksObjId(String strMarksObjId) {
		this.strMarksObjId = strMarksObjId;
	}
	public String getStrFormId() {
		return strFormId;
	}
	public void setStrFormId(String strFormId) {
		this.strFormId = strFormId;
	}
	public String getAdmissionYear() {
		return admissionYear;
	}
	public void setAdmissionYear(String admissionYear) {
		this.admissionYear = admissionYear;
	}
	public String getFormName() {
		return formName;
	}
	public void setFormName(String formName) {
		this.formName = formName;
	}
	public String getCourseClass() {
		return courseClass;
	}
	public void setCourseClass(String courseClass) {
		this.courseClass = courseClass;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	
	
}
