package com.qts.icam.model.tender;

public class TenderCategory {
	
	private String tenderCategoryCode;
	private String tenderCategoryName;
	
	public String getTenderCategoryCode() {
		return tenderCategoryCode;
	}
	public void setTenderCategoryCode(String tenderCategoryCode) {
		this.tenderCategoryCode = tenderCategoryCode;
	}
	public String getTenderCategoryName() {
		return tenderCategoryName;
	}
	public void setTenderCategoryName(String tenderCategoryName) {
		this.tenderCategoryName = tenderCategoryName;
	}
	
	
}
