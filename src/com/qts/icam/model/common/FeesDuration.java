package com.qts.icam.model.common;

public class FeesDuration {

	private String feesDurationObjectId;
	private String feesDurationCode;
	private String feesDurationDesc;
	private String feesDurationName;
	private String updatedBy;
	
	public String getFeesDurationObjectId() {
		return feesDurationObjectId;
	}
	public void setFeesDurationObjectId(String feesDurationObjectId) {
		this.feesDurationObjectId = feesDurationObjectId;
	}
	public String getFeesDurationCode() {
		return feesDurationCode;
	}
	public void setFeesDurationCode(String feesDurationCode) {
		this.feesDurationCode = feesDurationCode;
	}
	public String getFeesDurationDesc() {
		return feesDurationDesc;
	}
	public void setFeesDurationDesc(String feesDurationDesc) {
		this.feesDurationDesc = feesDurationDesc;
	}
	public String getFeesDurationName() {
		return feesDurationName;
	}
	public void setFeesDurationName(String feesDurationName) {
		this.feesDurationName = feesDurationName;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	
}
