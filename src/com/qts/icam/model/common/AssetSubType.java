package com.qts.icam.model.common;

public class AssetSubType {
	
	private int assetSubTypeId;
	private String objectId;
	private String updatedBy;
	private String assetSubTypeCode;
	private String assetSubTypeName;
	private String assetSubTypeDesc;
	private String assetType;
	private String status;
	
	public int getAssetSubTypeId() {
		return assetSubTypeId;
	}
	public void setAssetSubTypeId(int assetSubTypeId) {
		this.assetSubTypeId = assetSubTypeId;
	}
	public String getObjectId() {
		return objectId;
	}
	public void setObjectId(String objectId) {
		this.objectId = objectId;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	public String getAssetSubTypeCode() {
		return assetSubTypeCode;
	}
	public void setAssetSubTypeCode(String assetSubTypeCode) {
		this.assetSubTypeCode = assetSubTypeCode;
	}
	public String getAssetSubTypeName() {
		return assetSubTypeName;
	}
	public void setAssetSubTypeName(String assetSubTypeName) {
		this.assetSubTypeName = assetSubTypeName;
	}
	public String getAssetSubTypeDesc() {
		return assetSubTypeDesc;
	}
	public void setAssetSubTypeDesc(String assetSubTypeDesc) {
		this.assetSubTypeDesc = assetSubTypeDesc;
	}
	public String getAssetType() {
		return assetType;
	}
	public void setAssetType(String assetType) {
		this.assetType = assetType;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
