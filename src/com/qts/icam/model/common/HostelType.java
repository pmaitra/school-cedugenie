package com.qts.icam.model.common;

public class HostelType {
	
	private String hostelTypeObjectId;
	private int hostelTypeSerialId;
	private String hostelTypeCode;
	private String hostelTypeDesc;
	private String updatedBy;
	private String hostelTypeName;
	
	public String getHostelTypeObjectId() {
		return hostelTypeObjectId;
	}
	public void setHostelTypeObjectId(String hostelTypeObjectId) {
		this.hostelTypeObjectId = hostelTypeObjectId;
	}
	public int getHostelTypeSerialId() {
		return hostelTypeSerialId;
	}
	public void setHostelTypeSerialId(int hostelTypeSerialId) {
		this.hostelTypeSerialId = hostelTypeSerialId;
	}
	public String getHostelTypeCode() {
		return hostelTypeCode;
	}
	public void setHostelTypeCode(String hostelTypeCode) {
		this.hostelTypeCode = hostelTypeCode;
	}
	public String getHostelTypeDesc() {
		return hostelTypeDesc;
	}
	public void setHostelTypeDesc(String hostelTypeDesc) {
		this.hostelTypeDesc = hostelTypeDesc;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	public String getHostelTypeName() {
		return hostelTypeName;
	}
	public void setHostelTypeName(String hostelTypeName) {
		this.hostelTypeName = hostelTypeName;
	}
}
