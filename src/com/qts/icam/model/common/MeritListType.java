package com.qts.icam.model.common;

public class MeritListType {
	private int meritListTypeId;
	private String objectId;
	private String meritListTypeCode;
	private String meritListTypeDesc;
	private String meritListTypeName;
	private String updatedBy;
	private String status;
	
	public int getMeritListTypeId() {
		return meritListTypeId;
	}
	public void setMeritListTypeId(int meritListTypeId) {
		this.meritListTypeId = meritListTypeId;
	}
	public String getObjectId() {
		return objectId;
	}
	public void setObjectId(String objectId) {
		this.objectId = objectId;
	}
	public String getMeritListTypeCode() {
		return meritListTypeCode;
	}
	public void setMeritListTypeCode(String meritListTypeCode) {
		this.meritListTypeCode = meritListTypeCode;
	}
	public String getMeritListTypeDesc() {
		return meritListTypeDesc;
	}
	public void setMeritListTypeDesc(String meritListTypeDesc) {
		this.meritListTypeDesc = meritListTypeDesc;
	}
	public String getMeritListTypeName() {
		return meritListTypeName;
	}
	public void setMeritListTypeName(String meritListTypeName) {
		this.meritListTypeName = meritListTypeName;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
