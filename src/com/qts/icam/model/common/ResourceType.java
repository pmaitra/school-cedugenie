package com.qts.icam.model.common;

public class ResourceType {
	private String resourceTypeObjectId;
	private String resourceTypeCode;
	private String resourceTypeDesc;
	private String resourceTypeName;
	private String updatedBy;
	
	//-------------setter&getter-----------

	public String getResourceTypeObjectId() {
		return resourceTypeObjectId;
	}
	public void setResourceTypeObjectId(String resourceTypeObjectId) {
		this.resourceTypeObjectId = resourceTypeObjectId;
	}
	public String getResourceTypeCode() {
		return resourceTypeCode;
	}
	public void setResourceTypeCode(String resourceTypeCode) {
		this.resourceTypeCode = resourceTypeCode;
	}
	public String getResourceTypeDesc() {
		return resourceTypeDesc;
	}
	public void setResourceTypeDesc(String resourceTypeDesc) {
		this.resourceTypeDesc = resourceTypeDesc;
	}
	public String getResourceTypeName() {
		return resourceTypeName;
	}
	public void setResourceTypeName(String resourceTypeName) {
		this.resourceTypeName = resourceTypeName;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	
	
}
