package com.qts.icam.model.common;



public class Organization {
	private String organizationObjectId;
	private String organizationCode;
	private String organizationDesc;
	private String updatedBy;
	private String organizationName;
	private String organizationWebSite;
	private String organizationContactNo;
	private String fromDate;
	private String toDate;
	private String workExpYear;
	private String workExpMonth;
	private String costToCompany;
	private String userId;
	
	public String getOrganizationObjectId() {
		return organizationObjectId;
	}
	public void setOrganizationObjectId(String organizationObjectId) {
		this.organizationObjectId = organizationObjectId;
	}
	public String getOrganizationCode() {
		return organizationCode;
	}
	public void setOrganizationCode(String organizationCode) {
		this.organizationCode = organizationCode;
	}
	public String getOrganizationDesc() {
		return organizationDesc;
	}
	public void setOrganizationDesc(String organizationDesc) {
		this.organizationDesc = organizationDesc;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	public String getOrganizationName() {
		return organizationName;
	}
	public void setOrganizationName(String organizationName) {
		this.organizationName = organizationName;
	}
	public String getOrganizationWebSite() {
		return organizationWebSite;
	}
	public void setOrganizationWebSite(String organizationWebSite) {
		this.organizationWebSite = organizationWebSite;
	}
	public String getFromDate() {
		return fromDate;
	}
	public void setFromDate(String fromDate) {
		this.fromDate = fromDate;
	}
	public String getToDate() {
		return toDate;
	}
	public void setToDate(String toDate) {
		this.toDate = toDate;
	}
	public String getOrganizationContactNo() {
		return organizationContactNo;
	}
	public void setOrganizationContactNo(String organizationContactNo) {
		this.organizationContactNo = organizationContactNo;
	}
	public String getWorkExpYear() {
		return workExpYear;
	}
	public void setWorkExpYear(String workExpYear) {
		this.workExpYear = workExpYear;
	}
	public String getWorkExpMonth() {
		return workExpMonth;
	}
	public void setWorkExpMonth(String workExpMonth) {
		this.workExpMonth = workExpMonth;
	}
	public String getCostToCompany() {
		return costToCompany;
	}
	public void setCostToCompany(String costToCompany) {
		this.costToCompany = costToCompany;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
	
}
