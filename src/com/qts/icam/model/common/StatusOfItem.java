package com.qts.icam.model.common;

public class StatusOfItem {
	private String objectId;
	private String updatedBy;
	private String statusOfItemCode;
	private String statusOfItemName;
	private String statusOfItemDesc;
	/**
	 * @return the objectId
	 */
	public String getObjectId() {
		return objectId;
	}
	/**
	 * @param objectId the objectId to set
	 */
	public void setObjectId(String objectId) {
		this.objectId = objectId;
	}
	/**
	 * @return the updatedBy
	 */
	public String getUpdatedBy() {
		return updatedBy;
	}
	/**
	 * @param updatedBy the updatedBy to set
	 */
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	/**
	 * @return the statusOfItemCode
	 */
	public String getStatusOfItemCode() {
		return statusOfItemCode;
	}
	/**
	 * @param statusOfItemCode the statusOfItemCode to set
	 */
	public void setStatusOfItemCode(String statusOfItemCode) {
		this.statusOfItemCode = statusOfItemCode;
	}
	/**
	 * @return the statusOfItemName
	 */
	public String getStatusOfItemName() {
		return statusOfItemName;
	}
	/**
	 * @param statusOfItemName the statusOfItemName to set
	 */
	public void setStatusOfItemName(String statusOfItemName) {
		this.statusOfItemName = statusOfItemName;
	}
	/**
	 * @return the statusOfItemDesc
	 */
	public String getStatusOfItemDesc() {
		return statusOfItemDesc;
	}
	/**
	 * @param statusOfItemDesc the statusOfItemDesc to set
	 */
	public void setStatusOfItemDesc(String statusOfItemDesc) {
		this.statusOfItemDesc = statusOfItemDesc;
	}
	
	
}
