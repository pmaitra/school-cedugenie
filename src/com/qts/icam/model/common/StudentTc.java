package com.qts.icam.model.common;

public class StudentTc {
	

	private String objectId;
	private String updatedBy;
	private String academicSsession;
	private String tcCause;
	private String className;
	private String streamName;
	private String section;
	private String registrationId;
	
	
	public String getObjectId() {
		return objectId;
	}
	public void setObjectId(String objectId) {
		this.objectId = objectId;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	public String getAcademicSsession() {
		return academicSsession;
	}
	public void setAcademicSsession(String academicSsession) {
		this.academicSsession = academicSsession;
	}
	public String getTcCause() {
		return tcCause;
	}
	public void setTcCause(String tcCause) {
		this.tcCause = tcCause;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getStreamName() {
		return streamName;
	}
	public void setStreamName(String streamName) {
		this.streamName = streamName;
	}
	public String getSection() {
		return section;
	}
	public void setSection(String section) {
		this.section = section;
	}
	public String getRegistrationId() {
		return registrationId;
	}
	public void setRegistrationId(String registrationId) {
		this.registrationId = registrationId;
	}


}
