package com.qts.icam.model.erp;

public class Publication {
	private String publicationObjId;
	private String updatedBy;
	private String publicationCode;
	private String publicationDesc;
	private String publicationName;
	private String dateOfPublication;
	private String coPublisher;
	private String status;	
	private String userId;
	
	public String getPublicationObjId() {
		return publicationObjId;
	}
	public void setPublicationObjId(String publicationObjId) {
		this.publicationObjId = publicationObjId;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	public String getPublicationCode() {
		return publicationCode;
	}
	public void setPublicationCode(String publicationCode) {
		this.publicationCode = publicationCode;
	}
	public String getPublicationDesc() {
		return publicationDesc;
	}
	public void setPublicationDesc(String publicationDesc) {
		this.publicationDesc = publicationDesc;
	}
	public String getPublicationName() {
		return publicationName;
	}
	public void setPublicationName(String publicationName) {
		this.publicationName = publicationName;
	}
	public String getDateOfPublication() {
		return dateOfPublication;
	}
	public void setDateOfPublication(String dateOfPublication) {
		this.dateOfPublication = dateOfPublication;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getCoPublisher() {
		return coPublisher;
	}
	public void setCoPublisher(String coPublisher) {
		this.coPublisher = coPublisher;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
	
	
}
