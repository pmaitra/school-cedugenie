package com.qts.icam.model.erp;

public class DesignationLevel {
	private String designationLevelObjectId;
	private int designationLevelId;
	private String designationLevelCode;
	private String designationLevelDesc;	
	private String designationLevelName;
	private String updatedBy;
	private String status;
	public String getDesignationLevelObjectId() {
		return designationLevelObjectId;
	}
	public void setDesignationLevelObjectId(String designationLevelObjectId) {
		this.designationLevelObjectId = designationLevelObjectId;
	}
	public int getDesignationLevelId() {
		return designationLevelId;
	}
	public void setDesignationLevelId(int designationLevelId) {
		this.designationLevelId = designationLevelId;
	}
	public String getDesignationLevelCode() {
		return designationLevelCode;
	}
	public void setDesignationLevelCode(String designationLevelCode) {
		this.designationLevelCode = designationLevelCode;
	}
	public String getDesignationLevelDesc() {
		return designationLevelDesc;
	}
	public void setDesignationLevelDesc(String designationLevelDesc) {
		this.designationLevelDesc = designationLevelDesc;
	}
	public String getDesignationLevelName() {
		return designationLevelName;
	}
	public void setDesignationLevelName(String designationLevelName) {
		this.designationLevelName = designationLevelName;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
