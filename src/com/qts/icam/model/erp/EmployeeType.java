package com.qts.icam.model.erp;

public class EmployeeType {
	private int employeeTypeId;
	private String employeeTypeObjectId;
	private String employeeTypeCode;
	private String employeeTypeDesc;
	private String updatedBy;
	private String employeeTypeName;
	private String status;
	public int getEmployeeTypeId() {
		return employeeTypeId;
	}
	public void setEmployeeTypeId(int employeeTypeId) {
		this.employeeTypeId = employeeTypeId;
	}
	public String getEmployeeTypeObjectId() {
		return employeeTypeObjectId;
	}
	public void setEmployeeTypeObjectId(String employeeTypeObjectId) {
		this.employeeTypeObjectId = employeeTypeObjectId;
	}
	public String getEmployeeTypeCode() {
		return employeeTypeCode;
	}
	public void setEmployeeTypeCode(String employeeTypeCode) {
		this.employeeTypeCode = employeeTypeCode;
	}
	public String getEmployeeTypeDesc() {
		return employeeTypeDesc;
	}
	public void setEmployeeTypeDesc(String employeeTypeDesc) {
		this.employeeTypeDesc = employeeTypeDesc;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	public String getEmployeeTypeName() {
		return employeeTypeName;
	}
	public void setEmployeeTypeName(String employeeTypeName) {
		this.employeeTypeName = employeeTypeName;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
