package com.qts.icam.model.hostel;

public class HouseResidenceMapping {

	private String houseResidenceMappingObjectId;
	private String updatedBy;
	private String houseName;
	private String residenceName;
	/**
	 * @return the houseResidenceMappingObjectId
	 */
	public String getHouseResidenceMappingObjectId() {
		return houseResidenceMappingObjectId;
	}
	/**
	 * @param houseResidenceMappingObjectId the houseResidenceMappingObjectId to set
	 */
	public void setHouseResidenceMappingObjectId(String houseResidenceMappingObjectId) {
		this.houseResidenceMappingObjectId = houseResidenceMappingObjectId;
	}
	/**
	 * @return the updatedBy
	 */
	public String getUpdatedBy() {
		return updatedBy;
	}
	/**
	 * @param updatedBy the updatedBy to set
	 */
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	/**
	 * @return the houseName
	 */
	public String getHouseName() {
		return houseName;
	}
	/**
	 * @param houseName the houseName to set
	 */
	public void setHouseName(String houseName) {
		this.houseName = houseName;
	}
	/**
	 * @return the residenceName
	 */
	public String getResidenceName() {
		return residenceName;
	}
	/**
	 * @param residenceName the residenceName to set
	 */
	public void setResidenceName(String residenceName) {
		this.residenceName = residenceName;
	}
	
	
	
}
