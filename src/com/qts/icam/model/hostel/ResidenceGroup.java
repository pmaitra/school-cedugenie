package com.qts.icam.model.hostel;

public class ResidenceGroup {

	private String residenceGroupObjectId;
	private String updatedBy;
	private String residenceGroupName;
	private String residenceGroupCode;
	/**
	 * @return the residenceGroupObjectId
	 */
	public String getResidenceGroupObjectId() {
		return residenceGroupObjectId;
	}
	/**
	 * @param residenceGroupObjectId the residenceGroupObjectId to set
	 */
	public void setResidenceGroupObjectId(String residenceGroupObjectId) {
		this.residenceGroupObjectId = residenceGroupObjectId;
	}
	/**
	 * @return the updatedBy
	 */
	public String getUpdatedBy() {
		return updatedBy;
	}
	/**
	 * @param updatedBy the updatedBy to set
	 */
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	/**
	 * @return the residenceGroupName
	 */
	public String getResidenceGroupName() {
		return residenceGroupName;
	}
	/**
	 * @param residenceGroupName the residenceGroupName to set
	 */
	public void setResidenceGroupName(String residenceGroupName) {
		this.residenceGroupName = residenceGroupName;
	}
	/**
	 * @return the residenceGroupCode
	 */
	public String getResidenceGroupCode() {
		return residenceGroupCode;
	}
	/**
	 * @param residenceGroupCode the residenceGroupCode to set
	 */
	public void setResidenceGroupCode(String residenceGroupCode) {
		this.residenceGroupCode = residenceGroupCode;
	}
	
	
}
