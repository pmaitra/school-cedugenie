package com.qts.icam.model.hostel;

public class HouseType {

	private String houseTypeObjectId;
	private String updatedBy;
	private String houseTypeCode;
	private String houseTypeName;
	/**
	 * @return the houseTypeObjectId
	 */
	public String getHouseTypeObjectId() {
		return houseTypeObjectId;
	}
	/**
	 * @param houseTypeObjectId the houseTypeObjectId to set
	 */
	public void setHouseTypeObjectId(String houseTypeObjectId) {
		this.houseTypeObjectId = houseTypeObjectId;
	}
	/**
	 * @return the updatedBy
	 */
	public String getUpdatedBy() {
		return updatedBy;
	}
	/**
	 * @param updatedBy the updatedBy to set
	 */
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	/**
	 * @return the houseTypeCode
	 */
	public String getHouseTypeCode() {
		return houseTypeCode;
	}
	/**
	 * @param houseTypeCode the houseTypeCode to set
	 */
	public void setHouseTypeCode(String houseTypeCode) {
		this.houseTypeCode = houseTypeCode;
	}
	/**
	 * @return the houseTypeName
	 */
	public String getHouseTypeName() {
		return houseTypeName;
	}
	/**
	 * @param houseTypeName the houseTypeName to set
	 */
	public void setHouseTypeName(String houseTypeName) {
		this.houseTypeName = houseTypeName;
	}
	
	
}
