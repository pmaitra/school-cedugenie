package com.qts.icam.model.hostel;

public class House {

	private String houseObjectId;
	private String updatedBy;
	private HouseType houseType;
	private String houseCode;
	private String houseName;
	/**
	 * @return the houseObjectId
	 */
	public String getHouseObjectId() {
		return houseObjectId;
	}
	/**
	 * @param houseObjectId the houseObjectId to set
	 */
	public void setHouseObjectId(String houseObjectId) {
		this.houseObjectId = houseObjectId;
	}
	/**
	 * @return the updatedBy
	 */
	public String getUpdatedBy() {
		return updatedBy;
	}
	/**
	 * @param updatedBy the updatedBy to set
	 */
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	/**
	 * @return the houseType
	 */
	public HouseType getHouseType() {
		return houseType;
	}
	/**
	 * @param houseType the houseType to set
	 */
	public void setHouseType(HouseType houseType) {
		this.houseType = houseType;
	}
	/**
	 * @return the houseCode
	 */
	public String getHouseCode() {
		return houseCode;
	}
	/**
	 * @param houseCode the houseCode to set
	 */
	public void setHouseCode(String houseCode) {
		this.houseCode = houseCode;
	}
	/**
	 * @return the houseName
	 */
	public String getHouseName() {
		return houseName;
	}
	/**
	 * @param houseName the houseName to set
	 */
	public void setHouseName(String houseName) {
		this.houseName = houseName;
	}
	
	
}
