package com.qts.icam.model.backoffice;

public class TimeTableConfigModel {

	private String teacherName;
	private String teacherUserId;
	private String teacherInitial;
	private String classSectionName;
	private String subjectName;
	private String noOfClasses;
	private String oldClassSection;
	private String oldsubjectName;
	
	public String getTeacherInitial() {
		return teacherInitial;
	}
	public void setTeacherInitial(String teacherInitial) {
		this.teacherInitial = teacherInitial;
	}
	public String getTeacherUserId() {
		return teacherUserId;
	}
	public void setTeacherUserId(String teacherUserId) {
		this.teacherUserId = teacherUserId;
	}
	public String getTeacherName() {
		return teacherName;
	}
	public void setTeacherName(String teacherName) {
		this.teacherName = teacherName;
	}
	public String getClassSectionName() {
		return classSectionName;
	}
	public void setClassSectionName(String classSectionName) {
		this.classSectionName = classSectionName;
	}
	public String getSubjectName() {
		return subjectName;
	}
	public void setSubjectName(String subjectName) {
		this.subjectName = subjectName;
	}
	public String getNoOfClasses() {
		return noOfClasses;
	}
	public void setNoOfClasses(String noOfClasses) {
		this.noOfClasses = noOfClasses;
	}
	public String getOldClassSection() {
		return oldClassSection;
	}
	public void setOldClassSection(String oldClassSection) {
		this.oldClassSection = oldClassSection;
	}
	public String getOldsubjectName() {
		return oldsubjectName;
	}
	public void setOldsubjectName(String oldsubjectName) {
		this.oldsubjectName = oldsubjectName;
	}
	
	
}
