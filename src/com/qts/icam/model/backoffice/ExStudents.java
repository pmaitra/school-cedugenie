package com.qts.icam.model.backoffice;

public class ExStudents {
	private int serialId;
	private String objectId;
	private String updatedBy;
	private String status;
	
	private String rollNumber;
	private String name;
	private String fatherName;
	private String motherName;
	private String joinStandard;
	private String lastStandard;
	private String lastYear;
	private String dateOfAdmission;
	private String dateOfBirth;
	private String email;
	private String mobile;
	private String studentCharacter;
		
	
	public int getSerialId() {
		return serialId;
	}
	public void setSerialId(int serialId) {
		this.serialId = serialId;
	}
	public String getObjectId() {
		return objectId;
	}
	public void setObjectId(String objectId) {
		this.objectId = objectId;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getRollNumber() {
		return rollNumber;
	}
	public void setRollNumber(String rollNumber) {
		this.rollNumber = rollNumber;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getFatherName() {
		return fatherName;
	}
	public void setFatherName(String fatherName) {
		this.fatherName = fatherName;
	}
	public String getMotherName() {
		return motherName;
	}
	public void setMotherName(String motherName) {
		this.motherName = motherName;
	}
	public String getJoinStandard() {
		return joinStandard;
	}
	public void setJoinStandard(String joinStandard) {
		this.joinStandard = joinStandard;
	}
	public String getLastStandard() {
		return lastStandard;
	}
	public void setLastStandard(String lastStandard) {
		this.lastStandard = lastStandard;
	}
	public String getLastYear() {
		return lastYear;
	}
	public void setLastYear(String lastYear) {
		this.lastYear = lastYear;
	}
	public String getDateOfAdmission() {
		return dateOfAdmission;
	}
	public void setDateOfAdmission(String dateOfAdmission) {
		this.dateOfAdmission = dateOfAdmission;
	}
	public String getDateOfBirth() {
		return dateOfBirth;
	}
	public void setDateOfBirth(String dateOfBirth) {
		this.dateOfBirth = dateOfBirth;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getStudentCharacter() {
		return studentCharacter;
	}
	public void setStudentCharacter(String studentCharacter) {
		this.studentCharacter = studentCharacter;
	}
}
