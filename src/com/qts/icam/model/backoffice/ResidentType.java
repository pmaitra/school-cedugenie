package com.qts.icam.model.backoffice;

public class ResidentType {

	private String residentTypeObjectId;
	private String updatedBy;
	private String residentTypeCode;
	private String residentTypeName;
	/**
	 * @return the residentTypeObjectId
	 */
	public String getResidentTypeObjectId() {
		return residentTypeObjectId;
	}
	/**
	 * @param residentTypeObjectId the residentTypeObjectId to set
	 */
	public void setResidentTypeObjectId(String residentTypeObjectId) {
		this.residentTypeObjectId = residentTypeObjectId;
	}
	/**
	 * @return the updatedBy
	 */
	public String getUpdatedBy() {
		return updatedBy;
	}
	/**
	 * @param updatedBy the updatedBy to set
	 */
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	/**
	 * @return the residentTypeCode
	 */
	public String getResidentTypeCode() {
		return residentTypeCode;
	}
	/**
	 * @param residentTypeCode the residentTypeCode to set
	 */
	public void setResidentTypeCode(String residentTypeCode) {
		this.residentTypeCode = residentTypeCode;
	}
	/**
	 * @return the residentTypeName
	 */
	public String getResidentTypeName() {
		return residentTypeName;
	}
	/**
	 * @param residentTypeName the residentTypeName to set
	 */
	public void setResidentTypeName(String residentTypeName) {
		this.residentTypeName = residentTypeName;
	}
	
	
}
