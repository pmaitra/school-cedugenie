package com.qts.icam.model.backoffice;

public class MajorEvents {
	
	private int majorEventId;
	private String majorEventObjId;
	private String majorEventName;
	private String majorEventCode;
	private String majorEventDesc;
	private String updatedBy;
	private String status;
	private String majorEventDate;
	private int serial;
	private String date;
	private String event;
	
	public int getMajorEventId() {
		return majorEventId;
	}
	public void setMajorEventId(int majorEventId) {
		this.majorEventId = majorEventId;
	}
	public String getMajorEventObjId() {
		return majorEventObjId;
	}
	public void setMajorEventObjId(String majorEventObjId) {
		this.majorEventObjId = majorEventObjId;
	}
	public String getMajorEventName() {
		return majorEventName;
	}
	public void setMajorEventName(String majorEventName) {
		this.majorEventName = majorEventName;
	}
	public String getMajorEventCode() {
		return majorEventCode;
	}
	public void setMajorEventCode(String majorEventCode) {
		this.majorEventCode = majorEventCode;
	}
	public String getMajorEventDesc() {
		return majorEventDesc;
	}
	public void setMajorEventDesc(String majorEventDesc) {
		this.majorEventDesc = majorEventDesc;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getMajorEventDate() {
		return majorEventDate;
	}
	public void setMajorEventDate(String majorEventDate) {
		this.majorEventDate = majorEventDate;
	}
	public int getSerial() {
		return serial;
	}
	public void setSerial(int serial) {
		this.serial = serial;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getEvent() {
		return event;
	}
	public void setEvent(String event) {
		this.event = event;
	}

}
