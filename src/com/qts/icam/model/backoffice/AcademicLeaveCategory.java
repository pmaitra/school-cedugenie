public class AcademicLeaveCategory {

	private String leaveCategoryObjectId;
	private String leaveCategoryCode;
	private String leaveCategoryDesc;
	private String leaveCategoryName;
	private String updatedBy;
	private int leaveTypeId;
	
	public String getLeaveCategoryObjectId() {
		return leaveCategoryObjectId;
	}
	public void setLeaveCategoryObjectId(String leaveCategoryObjectId) {
		this.leaveCategoryObjectId = leaveCategoryObjectId;
	}
	public String getLeaveCategoryCode() {
		return leaveCategoryCode;
	}
	public void setLeaveCategoryCode(String leaveCategoryCode) {
		this.leaveCategoryCode = leaveCategoryCode;
	}
	public String getLeaveCategoryDesc() {
		return leaveCategoryDesc;
	}
	public void setLeaveCategoryDesc(String leaveCategoryDesc) {
		this.leaveCategoryDesc = leaveCategoryDesc;
	}
	public String getLeaveCategoryName() {
		return leaveCategoryName;
	}
	public void setLeaveCategoryName(String leaveCategoryName) {
		this.leaveCategoryName = leaveCategoryName;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	public int getLeaveTypeId() {
		return leaveTypeId;
	}
	public void setLeaveTypeId(int leaveTypeId) {
		this.leaveTypeId = leaveTypeId;
	}
	
	
}
